CREATE DATABASE global_electronic_retail;

USE global_electronic_retail;

SET GLOBAL LOCAL_INFILE = 1;

CREATE TABLE customers(
    CustomerKey INT,
    Gender varchar(10),
    Name VARCHAR(100),
    City VARCHAR(100),
    State_Code VARCHAR(10),
    State VARCHAR(100),
    Zip_Code INT,
    Country VARCHAR(100),
    Continent VARCHAR(50),
    Birthday DATE
);

CREATE TABLE sales(
    Order_Number INT,
    Line_Item INT,
    Order_Date DATE,
    Delivery_Date DATE,
    CustomerKey INT,
    StoreKey INT,
    ProductKey INT,
    Quantity INT,
    Currency_Code VARCHAR(10)
);

CREATE TABLE products(
    Product_Key INT PRIMARY KEY,
    Product_Name VARCHAR(100),
    Brand VARCHAR(100),
    Color VARCHAR(100),
    Unit_Cost_USD FLOAT,	
    Unit_Price_USD FLOAT,
    Subcategory_Key varchar(10),
    Subcategory VARCHAR(100),	
    CategoryKey varchar(10),
    Category VARCHAR(100)
);


CREATE TABLE stores (
    StoreKey INT PRIMARY KEY,	
    Country VARCHAR(100),	
    State VARCHAR(100),	
    Square_Meters INT,
    Open_Date DATE
);

